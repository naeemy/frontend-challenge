export const Icon = React.forwardRef<SVGSVGElement, IconProps>(
  ({ className, size, variant, name, children, ...props }, ref) => {
    // If children are provided, render custom SVG content
    if (children) {
      return (
        <svg
          className={clsx(iconVariants({ size, variant }), className)}
          ref={ref}
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
          {...props}
        >
          {children}
        </svg>
      );
    }

    // If name is provided, render the corresponding icon

    if (name && IconComponents[name]) {
      const IconComponent = IconComponents[name];
      return (
        <IconComponent
          className={clsx(iconVariants({ size, variant }), className)}
          ref={ref}
          {...props}
        />
      );
    }

    // Fallback: render an empty SVG
    return (
      <svg
        className={clsx(iconVariants({ size, variant }), className)}
        ref={ref}
        fill="none"
        stroke="currentColor"
        viewBox="0 0 24 24"
        {...props}
      />
    );
  },
);

Icon.displayName = "Icon";
